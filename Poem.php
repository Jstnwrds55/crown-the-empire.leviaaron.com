    <h1>The Train</h1>
    <h5>By: Justin Edwards</h5>
    <p>I forgive you sweet girl,<br />
I forgive you cause I,<br />
Think of nothing that's worse,<br />
Worse than making you cry.</p>

<p>Sure you hurt me a lot,<br />
To that I'll admit,<br />
But I'm sick of this feeling,<br />
Sick of feeling like pit.</p>

<p>People told me to leave,<br />
Said to run far away,<br />
Far away from that girl,<br />
Though she wants you to stay.</p>

<p>So I tried and I tried,<br />
And I told you I'm done,<br />
Said I'm done with this game,<br />
Cause it just isn't fun.</p>

<p>I was told it would help,<br />
That I'd feel the weights lift,<br />
Off my shoulders and up,<br />
But instead the weight sits.</p>

<p>And no matter how much,<br />
I say don't respond,<br />
I can't help it cause you,<br />
You are my tightest bond.</p>

<p>I'm a sucker for love,<br />
I get that from my dad,<br />
My dad who agrees,<br />
There was something we had.</p>

<p>So I keep playing on,<br />
Into your warm fingers,<br />
Cause the feeling that we,<br />
Bring each other joy, lingers.</p>

<p>And I truly believe,<br />
One day that we,<br />
Could finally work,<br />
And we&rsquo;d finally be free.</p>

<p>Cause I've never been joyous,<br />
Up until I met you,<br />
And I've never been sadder,<br />
To be free from you too.</p>

<p>Which is why I'm so stuck,<br />
In this silly position,<br />
Cause our connection is greater,<br />
More than nuclear fission.</p>

<p>But for some reason you can't,<br />
You just can't trust me and,<br />
It hurts cause my hearts,<br />
Always been in your hand.</p>

<p>Cause I broke down the walls,<br />
That surrounded it well,<br />
And I gave it to you,<br />
Cause I thought that you fell,</p>

<p>In the same way as me,<br />
As fast as one does,<br />
When you see something there,<br />
Such a wonderful buzz.</p>

<p>So you're left with my heart,<br />
Now look me in the eye,<br />
And protect it with care,<br />
Or I fear it may die.</p>

<p>Cause I haven't felt this,<br />
Haven't felt this before,<br />
This wonderful joy,<br />
That makes all else a bore.</p>

<p>Cause I love spending time,<br />
With you when I can,<br />
And insisting that I,<br />
Am your number one man.</p>

<p>Cause I think that it's true,<br />
And you tell me the same,<br />
But you&rsquo;re scared that you&rsquo;ll fall,<br />
And I&rsquo;ll leave you in pain.</p>

<p>Which I don&rsquo;t understand,<br />
No I can&rsquo;t comprehend,<br />
Who could leave you like that,<br />
A broken heart to mend.</p>

<p>Who could look at this girl,<br />
Stare into her blue eyes,<br />
And not see that inside,<br />
A part of her dies,</p>

<p>When somebody leaves,<br />
Leaves her there all alone,<br />
Slowly turning her heart,<br />
Turning right into stone.</p>

<p>So that all that she sees,<br />
Are not people but pawns,<br />
In the game that she plays,<br />
To survive till she&rsquo;s gone.</p>

<p>What a sad look on love,<br />
But it won&rsquo;t remain,<br />
If you trust me now,<br />
Trust me to take the pain,</p>

<p>And bury it down,<br />
Bury down deep below,<br />
Where I buried my pain,<br />
Buried it long ago.</p>

<p>Cause it never subsides,<br />
No not on it&rsquo;s own,<br />
It takes two for that battle,<br />
Trust me, I tried it alone.</p>

<p>Cause I was like you,<br />
Afraid of what&rsquo;s real,<br />
Cause I never knew,<br />
What it felt like to feel,</p>

<p>So strongly for someone,<br />
That throughout your day,<br />
They pop in your head,<br />
And you grin right away.</p>

<p>Cause something about,<br />
The way that they act,<br />
Makes you forget,<br />
How your heart was ransacked.</p>

<p>And to be that for you,<br />
To be your escape,<br />
Would elate me so much,<br />
My head would pop like a grape.</p>

<p>Cause for some reason I can&rsquo;t,<br />
Just look at you sit,<br />
In your sadness without,<br />
Feeling a sliver of guilt.</p>

<p>If I was to just watch,<br />
And not lend a hand,<br />
To her cause I can&rsquo;t,<br />
Watch her sit there be sad.</p>

<p>So I ask one more time,<br />
If you could just trust,<br />
This nerd to come in,<br />
And your sad heart, adjust.</p>

<p>Cause I want nothing else,<br />
Than to be close with you,<br />
But at this point I don&rsquo;t,<br />
No I don&rsquo;t have a clue,</p>

<p>How to make that dream,<br />
That dream that I have,<br />
Come to life so that we,<br />
Can just sit back and laugh,</p>

<p>At the times where we split,<br />
Where we went on our own,<br />
Yet each time gravitated,<br />
To the other one&rsquo;s home.</p>

<p>Cause there&rsquo;s something about,<br />
The way that we are,<br />
No matter our quarrels,<br />
We never get far,</p>

<p>Before we run back,<br />
To the other one&rsquo;s arms,<br />
Getting swept off our feet,<br />
By the other one&rsquo;s charms.</p>

<p>And you know that I&rsquo;d,<br />
Have it no other way,<br />
All I wish is that you'd,<br />
Look at me now and say,</p>

<p>&quot;I love you, ya butt,<br />
And I just don&rsquo;t know why,<br />
Cause you really do suck,<br />
And you&rsquo;re not a cool guy</p>

<p>But I do kinda like,<br />
The way that you act,<br />
And you make me feel swell,<br />
When I&rsquo;m feeling cracked.</p>

<p>So I think that I&rsquo;ll run,<br />
With this feeling we share,<br />
Yeah I&rsquo;ll run with it now,<br />
Cause it wouldn&rsquo;t be fair,</p>

<p>To deny my frail heart,<br />
This chance of revival,<br />
When a healthy heart&rsquo;s,<br />
A large part of survival</p>

<p>Cause I trust you&rsquo;ll be soft,<br />
And you&rsquo;ll treat me so nice,<br />
Take me out on some dates,<br />
Maybe get some fried rice,</p>

<p>Cause this won&rsquo;t end in sadness,<br />
To that I attest,<br />
Cause we&rsquo;ll get to the end,<br />
Just like the rest.&quot;</p>

<p>Maybe it&rsquo;s wishful thinking,<br />
It&rsquo;s starting to seem,<br />
That maybe you don&rsquo;t,<br />
Just no more have that gleam.</p>

<p>But I do still believe,<br />
That you are still there,<br />
That joyful girl,<br />
That I know will care,</p>

<p>Cause I&rsquo;ve seen her before,<br />
And it&rsquo;s something that I,<br />
Can&rsquo;t accept has just gone,<br />
Gone and went all awry.</p>

<p>Cause you know that I only,<br />
Accept what is great,<br />
And to me that is you,<br />
A you empty of hate.</p>

<p>So I&rsquo;ll fight for that bit,<br />
You know that I will,<br />
Cause you deserve that from me,<br />
Cause you know that I still,</p>

<p>Care for you and won&rsquo;t,<br />
More like can&rsquo;t stop the feels,<br />
It&rsquo;s a train that can wreck,<br />
But get back on it&rsquo;s wheels</p>

<p>And it has and it will,<br />
Continue to move,<br />
As long as you help,<br />
And don&rsquo;t throw off it&rsquo;s groove.</p>

<p>Cause I think in the end,<br />
That it all will work out,<br />
Cause you know working out,<br />
Is what I&rsquo;m about.</p>

<p>So I&rsquo;m back with a vengeance,<br />
And this huge list of lines,<br />
In hopes that you and me,<br />
Can go back to being fine,</p>

<p>More like better than fine,<br />
We could take on the world,<br />
Together with our flag,<br />
Of happiness unfurled.</p>

<p>If you open your eyes,<br />
And you look at me straight,<br />
And realize that even though,<br />
Neither of us believes fate,</p>

<p>We met for a reason,<br />
And it wasn&rsquo;t just cheer,<br />
No it&rsquo;s so that we can be,<br />
Together right here.</p>

<p>I know that I said,<br />
I was done with it all,<br />
But yet here I am,<br />
On my feet, on the ball,</p>

<p>And I&rsquo;m ready to hop,<br />
Back onto that train,<br />
Cause it&rsquo;s leaving the station,<br />
The station of pain,</p>

<p>And it&rsquo;s headed for you,<br />
Going in for the win,<br />
So I&rsquo;ll see you soon Kayles,<br />
See you soon with a grin :D</p>

    
    
